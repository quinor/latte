

def black(p):
    return f"\033[1;30m{p}\033[1;0m"

def red(p):
    return f"\033[1;31m{p}\033[1;0m"

def green(p):
    return f"\033[1;32m{p}\033[1;0m"

def yellow(p):
    return f"\033[1;33m{p}\033[1;0m"

def blue(p):
    return f"\033[1;34m{p}\033[1;0m"

def purple(p):
    return f"\033[1;35m{p}\033[1;0m"

def cyan(p):
    return f"\033[1;36m{p}\033[1;0m"

def white(p):
    return f"\033[1;37m{p}\033[1;0m"
